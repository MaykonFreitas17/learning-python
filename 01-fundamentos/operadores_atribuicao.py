# Recebe ...
a = 2 # "a" recebe o valor de 2

# atribuição aditiva
a = a + 1 
a += 1 # outra maneira de fazer a atribuição de cima

# atribuição subtrativa
a = a - 1 
a -= 1 # outra maneira de fazer a atribuição de cima

# atribuição multiplicativa
a = a * 1 
a *= 1 # outra maneira de fazer a atribuição de cima

# atribuição divisiva
a = a / 1 
a /= 1 # outra maneira de fazer a atribuição de cima

# atribuição modular/modulativa
a = a % 1 
a %= 1 # outra maneira de fazer a atribuição de cima

# atribuição com potência
a = a ** 1 
a **= 1 # outra maneira de fazer a atribuição de cima

# atribuição divisiva inteira
a = a // 1 
a //= 1 # outra maneira de fazer a atribuição de cima