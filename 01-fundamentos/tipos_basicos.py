# Tipos básicos em Python

# String -> "ISSO É UM STRING"
nome = "Maykon"

# Booleano -> True ou False
solteiro = True
namorando = False

# Númericos -> Inteiros e Decimais/Float
idade = 20 # Inteiro
altura = 1.65 # Decimal

# Array/listas -> [..., ..., ...]
notas = [10, 8, 9, 10]

# Dicionarios -> { chave1: valor1, chave2: valor2 }
dicionario = { "nome": "Azir", "level": 20, "classe": "Mago" }

# None -> Nulo || Null
dano = None
